								<p style="text-align:justify;&gt;&lt;span class=" subtitulo"="">La<strong> Universidad Nacional Experimental de la Seguridad</strong> <strong>UNES</strong> nace como resultado de incansables luchas de activistas de derechos humanos, investigadores, docentes, funcionarias y funcionarios policiales, que apoyaron los procesos generados desde la <strong>Comisión Nacional para la Reforma Policial</strong> (<strong>CONAREPOL</strong>). </p>
<p style="text-align:justify;&gt;&lt;span class=" texto"="">La<strong> CONAREPOL</strong> fue creada por el Gobierno Bolivariano con el objeto de construir un nuevo modelo policial fundamentado en la voluntad popular que logró expresarse de manera plural y democrática por medio de una amplia consulta que recolectó y sistematizó las opiniones y proposiciones de miles de personas a lo largo y ancho de nuestro territorio nacional. Este ejercicio, abierto e incluyente, involucró a las propias instituciones policiales, a representaciones sociales diversas y a expertos en el tema de la seguridad (en el ámbito nacional e internacional).</p>
<p style="text-align:justify;&gt;&lt;span class=" texto"="">La Consulta Popular se sustentó en los principios de participación, transparencia, imparcialidad y corresponsabilidad y hoy en día se puede afirmar que fue un proceso que sirve como claro ejemplo de lo que el pueblo venezolano puede conquistar por medio de su participación activa en la elaboración de políticas públicas, planes y programas referidos a la seguridad ciudadana.</p>
<p style="text-align:justify;&gt;Este meritorio esfuerzo colectivo abrió la posibilidad de llamar la atención sobre temas referentes a la carrera policial, al currículo académico, a la gestión y eficiencia de los cuerpos policiales y mecanismos de rendición de cuentas, la necesidad de un mayor acercamiento de los cuerpos policiales a la comunidad, de aumentar la presencia policial y de mejorar la dotación de estas instituciones para lograr altos niveles de gestión y eficiencia, entre otros. La Comisión recomendó, entre otras cosas, incrementar la participación comunitaria en la contraloría y supervisión de sus cuerpos policiales; además, de proponer la creación de una instancia interministerial que diseñara los estándares de la formación policial.&lt;/p&gt;
&lt;p style=" text-align:justify;="">El <strong>Nuevo Modelo Policial </strong>propuesto como resultado de la voluntad popular fue refrendado a través de Ley Habilitante por el Presidente de la República Bolivariana de Venezuela, Hugo Chávez, recogido en el <strong>Decreto con Rango Valor y Fuerza de Ley Orgánica del Servicio de Policía y del Cuerpo de Policía Nacional</strong> (2008), establece (en su artículo 58) que Órgano Rector en conjunto con el Ministerio del Poder Popular con competencia en materia de Educación Superior determinará el diseño curricular y las políticas y acciones que garanticen la unidad del proceso de formación y el desarrollo profesional permanente que deberá aplicar la institución académica nacional responsable del sistema único de formación de las funcionarias y funcionarios policiales.</p>
<p style="text-align:justify;&gt;En el marco de la &lt;strong&gt;Misión Alma Mater&lt;/strong&gt;, se formaliza la creación de la UNES, en el año 2009; como la institución universitaria especializada en la profesionalización y el desarrollo integral de funcionarias y funcionarios de la seguridad ciudadana en el país.&lt;/p&gt;
